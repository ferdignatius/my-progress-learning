// tipe data
// Terdapat tujuh (7) tipe data primitif yang mendefinisikan suatu nilai dalam JavaScript. Ketujuh nilai tersebut antara lain: Undefined, Number, BigInt, String, Boolean, Null, dan Symbol.


// Undifined = tipe data kalau gk ada nilai


// Numbers
// Int
document.writeln("Lesson")
document.writeln(1)
document.writeln(1++)
document.writeln(++1)
document.writeln(1--)

// Float
document.writeln("Version")
document.writeln(3.14)

// BigInt
document.writeln(119855265625718n)
document.writeln(7n)


// string
document.writeln("Welcome to Javascript");
// string concatenation = menggabungkan string
let hi = "Hallo"
console.log(hi + hi)
// string interpolation = memasukkan string ke string template
console.log(`Nama saya ${hi}`)


// bool
document.writeln("Status")
document.writeln(true)


// Null
console.log(null)


// Symbol ->  digunakan sebagai nama property dari Object
console.log(Symbol("id"))