// var -> variable biasa
var x = 5

// let -> var dengan aturan scope
let y = "Hallo"

// const = constant -> variable yang tak dapat diubah lagi nilainys
const ID = 1

// Di ES6 dianjurkan menggunakan let dan const

// Penulisan variable
// declaration statement
let nama;
