package Basic.TipeData;

public class Variable {
    public static void main(String[] args) {
        // var di Java itu sama seperti let di JS
        var Nama = "Ferdi";
        var Angka = 1;
        System.out.println(Nama);
        System.out.println(Angka);

        // final sama aja seperti const di JS
        final String Asik = "Asik";
        final int Desimal = 5;
        System.out.println(Asik);
        System.out.println(Desimal);
    }
}
